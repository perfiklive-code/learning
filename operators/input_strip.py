# Запитуємо користувача ввести числа через пробіл
введені_значення = input("Введіть числа через пробіл: ")

# Розділяємо рядок на окремі частини (рядки) і перетворюємо їх у числа
список_чисел = list(map(float, введені_значення.split()))

# Обчислюємо суму
сума = sum(список_чисел)

# Виводимо результат
print("Сума введених чисел:", сума)